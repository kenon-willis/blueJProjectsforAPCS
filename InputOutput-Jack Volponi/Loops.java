public class Loops
{
   public static void main(){
       
       int[] myArray = new int[12];
       System.out.print("The length of the array is " + myArray.length);
       
       //for loop from 0-10
       for(int i=0; i<10; i++){
           
           //print each value as the loop iterates
           System.out.println("The value of the counter variable is: " + i);
           
       }
       
    }
}
